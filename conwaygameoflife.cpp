// Generate the next world
void generate(int currentboard[][BOARDCOLS], int nextboard[][BOARDCOLS],int ROWS,int COLS)
{
	for(int row = 0; row < ROWS; row++)
	{
		for(int col = 0; col < COLS; col++)
		{
			if(currentboard[row][col] == 1)
			{
				if(checkneighbors(currentboard, row, col, ROWS, COLS) < 2)
				{
					nextboard[row][col] = 0;
				}
				else if((checkneighbors(currentboard, row, col, ROWS, COLS) == 2) || (checkneighbors(currentboard, row, col, ROWS, COLS) == 3))
				{
					nextboard[row][col] = 1;
				}
				else if(checkneighbors(currentboard, row, col, ROWS, COLS) > 3)
				{
					nextboard[row][col] = 0;
				}
			}
			else
			{
				if(checkneighbors(currentboard, row, col, ROWS, COLS) == 3)
				{
					nextboard[row][col] = 1;
				}
			}
		}
	}
}

// check the neighbors
int checkneighbors(int board[][BOARDCOLS],int row,int col,int ROWS,int COLS)
{
	int count = 0;
	
	if(board[row-1][col-1] == 1)
	{
		count++;
	}
	if(board[row-1][col] == 1)
	{
		count++;
	}
	if(board[row-1][col+1] == 1)
	{
		count++;
	}
	if(board[row][col-1] == 1)
	{
		count++;
	}
	if(board[row][col+1] == 1)
	{
		count++;
	}
	if(board[row+1][col-1] == 1)
	{
		count++;
	}
	if(board[row+1][col] == 1)
	{
		count++;	
	}
	if(board[row+1][col+1] == 1)
	{
		count++;
	}
	return count;	
}

// copy one board to another
void copy(int source[][BOARDCOLS], int dest[][BOARDCOLS],int ROWS,int COLS)
{
	for(int ROWS = 0; ROWS < BOARDROWS; ROWS++)
	{
		for(int COLS = 0; COLS < BOARDCOLS; COLS++)
		{
			dest[ROWS][COLS] = source[ROWS][COLS];
		}
	}	
}

// display the board
void display(int board[][BOARDCOLS],int ROWS,int COLS)
{
	for(int ROWS = 0; ROWS < BOARDROWS; ROWS++)
	{
		for(int COLS = 0; COLS < BOARDCOLS; COLS++)
		{
			cout << board[ROWS][COLS];
		}
		cout << endl;
	}	
}
